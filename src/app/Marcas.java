    private void JBRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBRegistrarActionPerformed
        String marca = JTFMarca.getText();
        byte estado = (byte) JCBEstado.getSelectedIndex();
        int idproveedor = JCBProveedores.getSelectedIndex() + 1;
        
        try {
            Connection con = Conexion.getConexion();
            PreparedStatement ps = con.prepareStatement("INSERT INTO Marcas (Marca, Estado, FIDProveedor) VALUES (?, ?, ?)");
            ps.setString(1, marca);
            ps.setByte(2, estado);
            ps.setInt(3, idproveedor);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Marca Guardado");
            limpiar();
            cargarTabla();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_JBRegistrarActionPerformed

